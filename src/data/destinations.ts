export function getDestinationBySlug(slug: string): Destination | undefined {
  return destinations.find((d) => d.slug === slug);
}

export function getRelatedDestinations(slug: string, limit = 3): Destination[] {
  const current = getDestinationBySlug(slug);
  if (!current) return destinations.slice(0, limit);
  return destinations
    .filter((d) => d.slug !== slug)
    .map((d) => ({
      d,
      score: d.tags.filter((t) => current.tags.includes(t)).length,
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((x) => x.d);
}
